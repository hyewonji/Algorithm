'''
첫째 줄에 N(1 ≤ N ≤ 100)이 주어짐.
첫째 줄부터 2×N-1번째 줄까지 차례대로 별을 출력.

ex)
5
->
*        *
**      **
***    ***
****  ****
**********
****  ****
***    ***
**      **
*        *
'''


N= int(input())
for i in range(N*2):
  if (i+1)<=N:
    print('*'*(i+1)+' '*(N*2-(i+1)*2)+'*'*(i+1))
  else:
    print('*'*(N*2-(i+1))+' '*((i+1)*2-N*2)+'*'*(N*2-(i+1)))
